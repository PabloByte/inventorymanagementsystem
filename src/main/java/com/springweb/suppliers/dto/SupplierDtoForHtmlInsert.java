package com.springweb.suppliers.dto;

public class SupplierDtoForHtmlInsert {


   
    private Long id;

    private String name;
    public SupplierDtoForHtmlInsert(Long id, String name) {
      this.id = id;
      this.name = name;
    }
    public SupplierDtoForHtmlInsert() {
    }
    public Long getId() {
      return id;
    }
    public void setId(Long id) {
      this.id = id;
    }
    public String getName() {
      return name;
    }
    public void setName(String name) {
      this.name = name;
    }
    
    
    














}
